/**
 * EX01 - Contador de 1 até 20
 *
 * Enunciado:
 * Criar um programa que:
 * 1) Comece do número 1
 * 2) Use while
 * 3) Conte até 20
 * 4) Mostre todos os números
 */

public class Ex01Contador {

    public static void main(String[] args) {

        int numero = 1;

        while (numero <= 20) {
            System.out.println(numero);
            numero++;
        }
    }
}
